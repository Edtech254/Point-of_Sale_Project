    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        /*Control Structure*/
 /*Maize*/
        if (maize.isSelected() == (true)) {
            double Qty1 = Double.parseDouble(Q1.getText());
            double price = Qty1 * 187;
            String cost = String.format("%.2f", price);
            maizetext.setText(cost);
        } else {
            maizetext.setText("0");
        }
        /*Sugar*/
        if (sugar.isSelected() == (true)) {
            double Qty2 = Double.parseDouble(Q2.getText());
            double price = Qty2 * 260;
            String cost = String.format("%.2f", price);
            sugartext.setText(cost);
        } else {
            sugartext.setText("0");
        }
        /*Wheat*/
        if (wheat.isSelected() == (true)) {
            double Qty3 = Double.parseDouble(Q3.getText());
            double price = Qty3 * 198;
            String cost = String.format("%.2f", price);
            wheattext.setText(cost);
        } else {
            wheattext.setText("0");
        }
        /*Cooking oil*/
        if (oil.isSelected() == (true)) {
            double Qty4 = Double.parseDouble(Q4.getText());
            double price = Qty4 * 750;
            String cost = String.format("%.2f", price);
            oiltext.setText(cost);
        } else {
            oiltext.setText("0");
        }
        /*Rice*/
        if (rice.isSelected() == (true)) {
            double Qty5 = Double.parseDouble(Q5.getText());
            double price = Qty5 * 210;
            String cost = String.format("%.2f", price);
            ricetext.setText(cost);
        } else {
            ricetext.setText("0");
        }
        /*Soap*/
        if (soap.isSelected() == (true)) {
            double Qty6 = Double.parseDouble(Q6.getText());
            double price = Qty6 * 180;
            String cost = String.format("%.2f", price);
            soaptext.setText(cost);
        } else {
            soaptext.setText("0");
        }

        /*Initializing or setting the variables*/
        double maize_price = Double.parseDouble(maizetext.getText());
        double sugar_price = Double.parseDouble(sugartext.getText());
        double wheat_price = Double.parseDouble(wheattext.getText());
        double oil_price = Double.parseDouble(oiltext.getText());
        double rice_price = Double.parseDouble(ricetext.getText());
        double soap_price = Double.parseDouble(soaptext.getText());
        
        /*Gross price calculations*/
        double gross_price = maize_price + sugar_price + wheat_price + oil_price + rice_price + soap_price;
        double tax = gross_price * 0.16;
        double pre_tax = gross_price - tax;

        /*Converting the int variables to String variable*/
        String total_price = String.format("%.2f", gross_price);
        String added_tax = String.format("%.2f", tax);
        String taxable = String.format("%.2f", pre_tax);

        /*Computations*/
        totalCost.setText(total_price);
        VAT.setText(added_tax);
        preTax.setText(taxable);

        /*Discount computations*/
        if (gross_price > 1000) {
            double net_price = gross_price * 0.98;
            double discount = 0.02 * gross_price;
            String payable_amount = String.format("%.2f", net_price);
            String discount_earned = String.format("%.2f", discount);

            paidCash.setText(payable_amount);
            DCT.setText(discount_earned);
        } else {
            double net_price = gross_price;
            String payable_amount = String.format("%.2f", net_price);
            paidCash.setText(payable_amount);
            DCT.setText("No Discount!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

