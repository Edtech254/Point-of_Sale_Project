    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        /*Setting variables and change calculation*/
        double cost = Double.parseDouble(paidCash.getText());
        double cash = Double.parseDouble(cashtext.getText());
        double Remaining = cash - cost;

        /*Change decision*/
        if (cash >= cost) {
            String Remaining_amount = String.format("%.2f", Remaining);
            changetext.setText(Remaining_amount);
        } else {
            changetext.setText("Insufficient\n Cash");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

